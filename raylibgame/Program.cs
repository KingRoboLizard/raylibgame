void checkFloorRoom()
{
    if (Player.y > Raylib.GetScreenHeight())
    {
        floor++;
        Player.y = 0;
    }
    else if (Player.y < -ys)
    {
        floor--;
        Player.y = Raylib.GetScreenHeight();
    }

    if (Player.x + xs / 2 > Raylib.GetScreenWidth())
    {
        room++;
        Player.x = 0 - xs / 2;
    }
    else if (Player.x + xs / 2 < 0)
    {
        room--;
        Player.x = Raylib.GetScreenWidth() - xs / 2;
    }
}

